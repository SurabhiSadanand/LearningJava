package ArithmeticOperators;

public class Rectangle {

	public static void main(String[] args) {
		double length = 10.0;
		double breadth = 15.5;
		double perimeter = 2 * (length + breadth);
		double area = length * breadth;
		System.out.println("The length of the rectangle: " + length);
		System.out.println("\nThe breadth of the rectangle: " + breadth);
		System.out.println("\nThe perimeter of the rectangle: " + perimeter);
		System.out.println("\nThe area of the rectangle: " + area);
	}

}
