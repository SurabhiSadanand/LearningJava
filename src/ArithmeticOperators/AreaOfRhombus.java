package ArithmeticOperators;

public class AreaOfRhombus {

	public static void main(String[] args) {
		double d1= 15;
		double d2= 18;
		double area = (d1*d2)/2;
		System.out.println("Diagonal 1 of rhombus: "+d1);
		System.out.println("Diagonal 2 of rhombus: "+d2);
		System.out.println("Area of rhombus: "+area);
	}

}
