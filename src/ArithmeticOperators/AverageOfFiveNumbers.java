package ArithmeticOperators;

public class AverageOfFiveNumbers {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=30;
		int d=40;
		int e=50;
		double avg=0;
		avg = (a+b+c+d+e)/5;
        System.out.println("Five numbers: "+ a+","+b+","+c+","+d+","+e);
        System.out.println("Average: "+avg);
	}

}
