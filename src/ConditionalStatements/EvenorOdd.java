package ConditionalStatements;

public class EvenorOdd {

	public static void main(String[] args) {
		int number = 20;
		System.out.println("Entered number is:"+ number);
		
		if (number%2==0) {
			System.out.println("Number is even");
		}
		else {
			System.out.println("Number  is odd");
		}
	}

}
